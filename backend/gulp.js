/*--------------*/
/*  HOW TO GULP  */
/*--------------*/
/*
#1 NEED NPM INSTALL GULP IN /NODE_MODULES

#2 NEED GULPFILE.JS

#3 gulp --tasks
			shows avail actions
			
#2 VANILLA GULP IS WEAK, NPM INSTALL MORE STUFF
	Autoprefixer
		add -moz / -webkit to CSS automatically

	gulp-browser-sync
		live reload browser

	gulp-concat
		combine multiple files into one Javascript file (Reduce network requests)
		
	gulp-connect
		enable gulp to run server like nodejs

	gulp-iconfont
		convert SVGs to font for better use

	gulp-load-plugins
		load multiple gulp plugins at once

	gulp-plumber
		checks your gulpfile spacing/syntax

	gulp-minify-css
		minify CSS
	gulp-uglify
		minify JS

	gulp-sourcemaps
		enable reading of compressed/minified CSS/JS
		
	gulp-util
		utility functions such as logging
*/
